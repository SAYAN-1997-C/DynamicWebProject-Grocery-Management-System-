package master.DTO;

public class CashDalDTO {

	String cash_date;
	String billno;
	String dal_id;
	String quan;
	String price;
	public String getCash_date() {
		return cash_date;
	}
	public void setCash_date(String cash_date) {
		this.cash_date = cash_date;
	}
	public String getBillno() {
		return billno;
	}
	public void setBillno(String billno) {
		this.billno = billno;
	}
	public String getDal_id() {
		return dal_id;
	}
	public void setDal_id(String dal_id) {
		this.dal_id = dal_id;
	}
	public String getQuan() {
		return quan;
	}
	public void setQuan(String quan) {
		this.quan = quan;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	
}
