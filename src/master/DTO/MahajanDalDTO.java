package master.DTO;

public class MahajanDalDTO {
	
	String sdate;
	String mid;
	String dal_id;
	String quan;
	String price;
	public String getSdate() {
		return sdate;
	}
	public void setSdate(String sdate) {
		this.sdate = sdate;
	}
	public String getMid() {
		return mid;
	}
	public void setMid(String mid) {
		this.mid = mid;
	}
	public String getDal_id() {
		return dal_id;
	}
	public void setDal_id(String dal_id) {
		this.dal_id = dal_id;
	}
	public String getQuan() {
		return quan;
	}
	public void setQuan(String quan) {
		this.quan = quan;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	
}
