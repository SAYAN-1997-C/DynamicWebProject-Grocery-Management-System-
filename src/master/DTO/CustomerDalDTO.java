package master.DTO;

public class CustomerDalDTO {
	
	String cdate;
	String cid;
	String dal_id;
	String quan;
	String price;
	public String getCdate() {
		return cdate;
	}
	public void setCdate(String cdate) {
		this.cdate = cdate;
	}
	public String getCid() {
		return cid;
	}
	public void setCid(String cid) {
		this.cid = cid;
	}
	public String getDal_id() {
		return dal_id;
	}
	public void setDal_id(String dal_id) {
		this.dal_id = dal_id;
	}
	public String getQuan() {
		return quan;
	}
	public void setQuan(String quan) {
		this.quan = quan;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	
}
